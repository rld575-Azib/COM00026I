model = MyNet().to(device)
print(model)

# calculate loss for multiclass
loss_fn = nn.CrossEntropyLoss()
optimizer = torch.optim.Adam(model.parameters(), lr=1e-2)

def train_one_epoch(model, loader):
    # enable training mode to prevent complications with batchnorm
    model.train()
    running_loss = 0.0
    for images, labels in loader:
        images, labels = images.to(device), labels.to(device)

        optimizer.zero_grad()
        logits = model(images)
        loss = loss_fn(logits, labels)
        loss.backward()
        optimizer.step()

        running_loss += loss.item()
    return running_loss / len(loader)

@torch.no_grad()
def evaluate(model, loader):
    model.eval()
    correct, total = 0, 0
    for images, labels in loader:
        images, labels = images.to(device), labels.to(device)
        logits = model(images)
        preds = logits.argmax(dim=1)
        correct += (preds == labels).sum().item()
        total += labels.size(0)
    return correct / total

EPOCHS = 30

for epoch in range(EPOCHS):
    train_loss = train_one_epoch(model, train_loader)
    val_acc = evaluate(model, val_loader)
    print(f"epoch {epoch+1:2d} | train loss {train_loss:.3f} | val acc {val_acc:.3f}")

test_acc = evaluate(model, test_loader)
train_acc = evaluate(model, train_loader)
print(f"FINAL  train acc {train_acc:.3f} | test acc {test_acc:.3f}")
torch.save(model.state_dict(), "model.pth")
